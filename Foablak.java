    private void ellenor(int v){
        int valasz = v;
        
        if (!helyes) {
            tfKerdezo.setText("A játék véget ért!");
        }
        if (valasz == kerdezo.getKerdes().getHelyesvalasz()) {
            tfKerdezo.setText("A válasz helyes! Jöhet a következő?");
            nyeremeny--;
            kerdesSzam++;

        } else {

            int i = 14;
            if (nyeremeny <= 11 && nyeremeny > 6) {
                i = 10;
            } else if (nyeremeny <= 6 && nyeremeny > 0) {
                i = 5;
            } else if (nyeremeny == 0) {
                i = 0;
            }
            tfKerdezo.setText(" Rossz válasz, a játék véget ért, nyereményed:" + nyeremenyek[i]);
            helyes = false;

        }
        
    }
