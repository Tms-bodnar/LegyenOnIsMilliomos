    private void btnFelezoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFelezoActionPerformed
        if (kerdes == null && felezo == true) {
            JOptionPane.showMessageDialog(this, "Ezt elhasználtad a semmire!?", "Felező", JOptionPane.OK_OPTION);
        }
        int valasz = kerdes.getHelyesvalasz();
        if (valasz == 0 && felezo == true) {
            btnValasz1.setText("Ez biztos nem jó!");
            btnValasz3.setText("Ezt se válszd, mert megjárod!");
        }
        if (valasz == 1 && felezo == true) {
            btnValasz2.setText("Ez biztos nem jó!");
            btnValasz3.setText("Ezt se válszd, mert megjárod!");
        }
        if (valasz == 2 && felezo == true) {
            btnValasz0.setText("Ez biztos nem jó!");
            btnValasz3.setText("Ezt se válszd, mert megjárod!");
        }
        if (valasz == 3 && felezo == true) {
            btnValasz0.setText("Ez biztos nem jó!");
            btnValasz2.setText("Ezt se válszd, mert megjárod!");
        }
        felezo = false;
        
    }//GEN-LAST:event_btnFelezoActionPerformed

