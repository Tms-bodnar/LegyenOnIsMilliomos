    private void jmUjJatekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmUjJatekActionPerformed
        kerdezo = new Kerdezo();
        kerdezo.setHelyes(true);
        kerdesSzam = 1;
        nyeremeny = 15;
        helyes = true;

        try {
            kerdesek = model.getAllKerdes();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Adatbázis hiba!", JOptionPane.ERROR_MESSAGE);
        }
        kerdezo.setKerdesek(kerdesek);
        btnTelefon.setSelected(false);
        btnKozonseg.setSelected(false);
        btnFelezo.setSelected(false);
                
        jatek();
    }

    public void jatek() {
        if (helyes) {
            kerdes = kerdezo.getKerdezo(kerdesSzam - 1);
            jProgressBar2.setValue(kerdesSzam);
            lstNyeremeny.setSelectedIndex(15 - kerdesSzam);

            if (kerdesSzam == 1) {
                tfKerdezo.setText("Az első kérdés:");
            } else {
                tfKerdezo.setText(" A " + (kerdesSzam) + ". kérdés a következő:");
            }

            tfKerdes.setText(kerdes.getKerdes());
            btnValasz0.setText("<html>"+kerdes.getValasz0()+"</html>");
            btnValasz1.setText("<html>"+kerdes.getValasz1()+"</html>");
            btnValasz2.setText("<html>"+kerdes.getValasz2()+"</html>");
            btnValasz3.setText("<html>"+kerdes.getValasz3()+"</html>");
        }
    }//GEN-LAST:event_jmUjJatekActionPerformed

